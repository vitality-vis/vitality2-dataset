#!/usr/bin/env python3
"""
Upload records from paper enriched/missing directories to Zilliz.

The target collection is expected to use the schema created by
script/create_zilliz_collection.py: paper_uid VARCHAR primary key, nullable
DOI, nullable embedding, nullable UMAP, analyzed search_text, BM25 sparse
field generated by Zilliz, and paper metadata fields.
"""

from __future__ import annotations

import argparse
import hashlib
import json
import os
import re
from pathlib import Path
from typing import Any, Iterable

try:
    from create_zilliz_collection import PROJECT_ROOT, load_dotenv_file
except ModuleNotFoundError:
    from script.create_zilliz_collection import PROJECT_ROOT, load_dotenv_file


DEFAULT_COLLECTION = "paper_new"
DEFAULT_BATCH_SIZE = 500
DEFAULT_DATA_DIRS = (
    PROJECT_ROOT / "data" / "papers" / "enriched",
    PROJECT_ROOT / "data" / "papers" / "missing",
)
LEGACY_CITATION_FIELD = "citation" + "Counts"
LEGACY_FULL_PAPER_FIELD = "full" + "paper"


def iter_json_files(paths: Iterable[Path]) -> Iterable[Path]:
    for folder in paths:
        yield from sorted(folder.glob("*.json"))


def display_path(path: Path) -> str:
    try:
        return str(path.resolve().relative_to(PROJECT_ROOT))
    except ValueError:
        return str(path)


def paper_dirs(papers_dir: Path | None) -> tuple[Path, Path]:
    if papers_dir is None:
        return DEFAULT_DATA_DIRS
    return (papers_dir / "enriched", papers_dir / "missing")


def load_records(path: Path) -> list[dict[str, Any]]:
    data = json.loads(path.read_text(encoding="utf-8"))
    if not isinstance(data, list):
        raise ValueError(f"Expected top-level JSON array: {path}")
    return data


def load_existing_uids(path: Path | None) -> set[str]:
    if path is None:
        return set()
    if not path.exists():
        raise SystemExit(f"Existing UID file does not exist: {path}")
    uids: set[str] = set()
    with path.open("r", encoding="utf-8") as handle:
        for raw_line in handle:
            uid = raw_line.strip()
            if uid and not uid.startswith("#"):
                uids.add(uid)
    return uids


def clean_str(value: Any, max_length: int, *, nullable: bool = False) -> str | None:
    if value is None:
        return None if nullable else ""
    text = str(value).strip()
    if not text and nullable:
        return None
    return text[:max_length]


def normalize_text(value: Any) -> str:
    return re.sub(r"\s+", " ", str(value or "")).strip()


def normalize_doi(value: Any) -> str:
    doi = normalize_text(value)
    lower = doi.lower()
    for prefix in (
        "https://doi.org/",
        "http://doi.org/",
        "https://dx.doi.org/",
        "http://dx.doi.org/",
        "doi:",
    ):
        if lower.startswith(prefix):
            return doi[len(prefix) :].strip()
    return doi


def build_paper_uid(record: dict[str, Any]) -> str:
    existing = normalize_text(record.get("paper_uid"))
    if existing:
        return existing[:1024]

    doi = normalize_doi(record.get("doi"))
    if doi:
        return f"doi:{doi.casefold()}"[:1024]

    dblp_key = normalize_text(record.get("dblp_key"))
    if dblp_key:
        return f"dblp:{dblp_key}"[:1024]

    raw = json.dumps(
        {
            "title": normalize_text(record.get("title")).casefold(),
            "authors": [normalize_text(author).casefold() for author in record.get("authors") or []],
            "year": normalize_text(record.get("year")),
            "dblp_source": normalize_text(record.get("dblp_source")).casefold(),
        },
        ensure_ascii=False,
        sort_keys=True,
        separators=(",", ":"),
    )
    return "fallback:" + hashlib.sha1(raw.encode("utf-8")).hexdigest()


def clean_int(value: Any, *, nullable: bool = False) -> int | None:
    if value is None or value == "":
        return None if nullable else 0
    try:
        return int(value)
    except (TypeError, ValueError):
        return None if nullable else 0


def clean_bool(value: Any) -> bool:
    if isinstance(value, bool):
        return value
    if isinstance(value, str):
        return value.strip().lower() in {"1", "true", "yes", "y"}
    return bool(value)


def get_first_present(record: dict[str, Any], *keys: str) -> Any:
    for key in keys:
        if key in record:
            return record[key]
    return None


def clean_str_list(value: Any, *, nullable: bool = False) -> list[str] | None:
    if value is None:
        return None if nullable else []
    if isinstance(value, str):
        items = [value]
    elif isinstance(value, list):
        items = value
    else:
        items = [value]

    cleaned = []
    for item in items[:256]:
        text = str(item).strip()
        if text:
            cleaned.append(text[:512])
    if not cleaned and nullable:
        return None
    return cleaned


def build_search_text(record: dict[str, Any]) -> str | None:
    parts: list[str] = []

    for key in ("title", "abstract"):
        value = record.get(key)
        if value:
            parts.append(str(value))

    for key in ("keywords", "authors"):
        value = record.get(key)
        if isinstance(value, list):
            parts.extend(str(item) for item in value if item)
        elif value:
            parts.append(str(value))

    text = " ".join(part.strip() for part in parts if part and part.strip()).lower()
    return text[:65535] if text else None


def to_entity(record: dict[str, Any]) -> dict[str, Any]:
    return {
        "paper_uid": build_paper_uid(record),
        "dblp_key": clean_str(record.get("dblp_key"), 1024, nullable=True),
        "doi": clean_str(record.get("doi"), 512, nullable=True),
        "embedding": None,
        "umap": None,
        "search_text": build_search_text(record),
        "title": clean_str(record.get("title"), 4096),
        "abstract": clean_str(record.get("abstract"), 65535, nullable=True),
        "authors": clean_str_list(record.get("authors")),
        "keywords": clean_str_list(record.get("keywords"), nullable=True),
        "source": clean_str(record.get("source"), 1024),
        "dblp_source": clean_str(record.get("dblp_source"), 1024),
        "year": clean_int(record.get("year")),
        "citation_count": clean_int(
            get_first_present(record, "citation_count", LEGACY_CITATION_FIELD),
            nullable=True,
        ),
        "full_paper": clean_bool(get_first_present(record, "full_paper", LEGACY_FULL_PAPER_FIELD)),
    }


def connect_collection(collection_name: str):
    from pymilvus import Collection, connections, utility

    load_dotenv_file(PROJECT_ROOT / ".env")
    uri = os.environ.get("ZILLIZ_URI")
    token = os.environ.get("ZILLIZ_TOKEN")
    if not uri or not token:
        raise SystemExit("Missing ZILLIZ_URI or ZILLIZ_TOKEN in environment or project .env.")

    connections.connect(uri=uri, token=token)
    if not utility.has_collection(collection_name):
        raise SystemExit(f"Collection does not exist: {collection_name}")
    return Collection(collection_name)


def validate_schema(collection) -> None:
    fields = {field.name: field for field in collection.schema.fields}
    expected = {
        "paper_uid",
        "dblp_key",
        "doi",
        "embedding",
        "umap",
        "search_text",
        "search_sparse",
        "title",
        "abstract",
        "authors",
        "keywords",
        "source",
        "dblp_source",
        "year",
        "citation_count",
        "full_paper",
    }
    missing = sorted(expected - set(fields))
    if missing:
        raise SystemExit(f"Collection schema is missing fields: {', '.join(missing)}")

    paper_uid_field = fields["paper_uid"]
    if not getattr(paper_uid_field, "is_primary", False):
        raise SystemExit("Collection field 'paper_uid' must be the primary key.")
    if getattr(paper_uid_field, "auto_id", False):
        raise SystemExit("Collection field 'paper_uid' must not use auto_id.")


def insert_batch(collection, batch: list[dict[str, Any]]) -> int:
    if not batch:
        return 0
    collection.insert(batch)
    inserted = len(batch)
    batch.clear()
    return inserted


def upload(args: argparse.Namespace) -> None:
    collection = connect_collection(args.collection)
    validate_schema(collection)

    existing_rows = 0
    collection.flush()
    if args.resume:
        existing_rows = int(collection.num_entities)
        if args.skip and args.skip != existing_rows:
            raise SystemExit(
                f"--resume found {existing_rows} existing entities, but --skip is {args.skip}. "
                "Use only --resume or pass the matching --skip value."
            )
        args.skip = existing_rows
        args.allow_non_empty = True
        print(f"Resume mode: collection has {existing_rows} entities; skipping first {args.skip} records.")

    if not args.allow_non_empty:
        if int(collection.num_entities) != 0:
            raise SystemExit(
                f"Collection {args.collection} already has {collection.num_entities} entities. "
                "Use --resume to continue from the current entity count, or --allow-non-empty "
                "if you intentionally want to append."
            )

    files = list(iter_json_files(paper_dirs(args.papers_dir)))
    if not files:
        raise SystemExit(f"No paper JSON files found for upload in {paper_dirs(args.papers_dir)}")
    existing_uids = load_existing_uids(args.existing_uid_file)
    if existing_uids:
        print(f"Loaded {len(existing_uids)} existing paper_uid values for upload filtering.", flush=True)
    scanned_rows = 0
    skipped_rows = 0
    skipped_existing_uid_rows = 0
    inserted_rows = 0
    batch: list[dict[str, Any]] = []

    for path in files:
        records = load_records(path)
        file_scanned = 0
        file_skipped = 0
        file_inserted = 0
        for record in records:
            scanned_rows += 1
            file_scanned += 1
            if scanned_rows <= args.skip:
                skipped_rows += 1
                file_skipped += 1
                continue
            entity = to_entity(record)
            if entity["paper_uid"] in existing_uids:
                skipped_rows += 1
                skipped_existing_uid_rows += 1
                file_skipped += 1
                continue
            batch.append(entity)
            if len(batch) >= args.batch_size:
                inserted = insert_batch(collection, batch)
                file_inserted += inserted
                inserted_rows += inserted
                print(
                    f"Inserted batch: total inserted this run {inserted_rows}, "
                    f"scanned {scanned_rows}, skipped {skipped_rows}",
                    flush=True,
                )
        print(
            f"{display_path(path)}: scanned {file_scanned}, "
            f"skipped {file_skipped}, inserted {file_inserted}",
            flush=True,
        )

    inserted_rows += insert_batch(collection, batch)
    collection.flush()

    print(f"Scanned rows: {scanned_rows}", flush=True)
    print(f"Skipped rows: {skipped_rows}", flush=True)
    print(f"Skipped existing paper_uid rows: {skipped_existing_uid_rows}", flush=True)
    print(f"Inserted rows in this run: {inserted_rows}", flush=True)
    print(f"Collection entities after flush: {collection.num_entities}", flush=True)


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Upload data/papers/enriched and data/papers/missing to Zilliz."
    )
    parser.add_argument("--collection", default=DEFAULT_COLLECTION)
    parser.add_argument(
        "--papers-dir",
        type=Path,
        default=None,
        help="Directory containing enriched/ and missing/. Defaults to data/papers.",
    )
    parser.add_argument(
        "--existing-uid-file",
        type=Path,
        default=None,
        help="Optional local paper_uid file. Records already listed here are skipped before insert.",
    )
    parser.add_argument("--batch-size", type=int, default=DEFAULT_BATCH_SIZE)
    parser.add_argument(
        "--skip",
        type=int,
        default=0,
        help="Skip this many records from the deterministic file order before inserting.",
    )
    parser.add_argument(
        "--allow-non-empty",
        action="store_true",
        help="Append even if the collection already contains entities.",
    )
    parser.add_argument(
        "--resume",
        action="store_true",
        help="Continue from the current collection entity count using the deterministic file order.",
    )
    return parser.parse_args()


def main() -> None:
    upload(parse_args())


if __name__ == "__main__":
    main()
